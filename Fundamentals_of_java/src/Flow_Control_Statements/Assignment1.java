public class Assignment1 {

	public static void main(String[] args) {

		int n=-12;
		if(n>0) {
			System.out.println("Given input is Positive");
		}else if(n<0){
			System.out.println("Given input is Negative");
		}else {
			System.out.println("Given input is Zero");
		}
	}

}
